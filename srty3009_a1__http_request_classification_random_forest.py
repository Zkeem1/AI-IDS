epsilons = [0.00, 0.01, 0.05, 0.10]

accuracy_results = []
f1_results = []

attack = ZooAttack(
    classifier=art_classifier,
    confidence=0.0,
    targeted=False,
    max_iter=10,
    nb_parallel=1
)

print("\nGenerating adversarial examples...")
X_zoo = attack.generate(X_test_subset)

for eps in epsilons:

    if eps == 0.00:
        X_adv = X_test_subset.copy()
    else:
        X_adv = (
            X_test_subset +
            eps * (X_zoo - X_test_subset)
        )

    y_pred_adv = model.predict(X_adv)

    acc = accuracy_score(
        y_test_subset,
        y_pred_adv
    )

    f1 = f1_score(
        y_test_subset,
        y_pred_adv,
        average='weighted'
    )

    accuracy_results.append(acc)
    f1_results.append(f1)

    print(
        f"Epsilon={eps:.2f} | "
        f"Accuracy={acc:.4f} | "
        f"Weighted F1={f1:.4f}"
    )

