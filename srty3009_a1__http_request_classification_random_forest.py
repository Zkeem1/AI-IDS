def predict_http_request(raw_row_dict):

    # Build dataframe
    row_df = pd.DataFrame([raw_row_dict])

    # Add missing columns
    for col in ml_features:
        if col not in row_df.columns:
            row_df[col] = 0

    # Match original feature order
    row_df = row_df[ml_features]

    # Apply preprocessing
    row_imp = loaded_imputer.transform(row_df)
    row_scaled = loaded_scaler.transform(row_imp)

    # Predict
    prediction = loaded_model.predict(row_scaled)[0]

    # Probabilities
    probabilities = loaded_model.predict_proba(row_scaled)[0]

    confidence = np.max(probabilities)

    predicted_label = target_encoder.inverse_transform(
        [prediction]
    )[0]

    scores = pd.Series(
        probabilities,
        index=target_encoder.classes_
    ).sort_values(ascending=False)

    print("Prediction:", predicted_label)
    print(f"Confidence: {confidence:.2%}")

    print("\nTop Probabilities")
    print(scores.head(3))

    return predicted_label, scores